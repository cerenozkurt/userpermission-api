     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Comment  $comment
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function update(User $user, Comment $comment)
    {
        return $user->id == $comment->user_id;
    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Comment  $comment
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function delete(User $user, Comment $comment)
    {
        switch ($user) {
            case $user->hasRole('superadmin'): //superadminse siler
                return TRUE;
                break;
            case $user->hasRole('admin'): //adminse rolu superadmin ve admin olmayanlar覺n commentini siler
                if ($user->id != $comment->users->id  && $comment->users->hasAnyRole(['superadmin', 'admin'])) {
                    return FALSE;
                }
                return TRUE;
                break;
            case $user->hasRole('editor'): //editorse sadece writerlar覺n commentlar覺n覺 siler
                if ($user->id != $comment->users->id  &&  $comment->users->hasAnyRole(['superadmin', 'admin', 'editor'])) {
                    return FALSE;
                }
                return TRUE;
                break;
            default: //writersa sadece kendi commentini siler
                if ($comment->users->id == $user->id) {
                    return TRUE;
                    break;
                }
                return FALSE;
        }
    }

